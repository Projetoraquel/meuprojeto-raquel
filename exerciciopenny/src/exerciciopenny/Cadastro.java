    private void botao1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao1ActionPerformed
        // TODO add your handling code here:
        Sapatos s = new Sapatos();
        s.setModelo(caixa.getText());
        s.setCor(caixa2.getText());
        s.setNum(caixa3.getText());
        if(caixa4.getSelectedItem().toString().equals("Sim") == true){
            s.setPreferido(true);
        }else{
            s.setPreferido(false);
        }
        Lista.add(s);
        Limpar();
    }//GEN-LAST:event_botao1ActionPerformed

    private void botao3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao3ActionPerformed
        // TODO add your handling code here:
        String nome = JOptionPane.showInputDialog("Digite o nome a ser pesquisado:");
        boolean encontrou = false;
        int posicaoachou = 0;
        
        for (Sapatos s : Lista){
            if (nome.equals(s.getModelo())){ 
                posicao = posicaoachou;
                caixa.setText(s.getModelo());
                caixa2.setText(s.getCor());   
                caixa3.setText(s.getNum());
                if (caixa4.action(null, evt) == true){
            caixa4.setSelectedIndex(1);
        }else{ 
            caixa4.setSelectedIndex(2);
        }
                encontrou = true;
                break;
            }posicaoachou++;
        }
        
            if (encontrou == true){
                JOptionPane.showMessageDialog(null,"Encontrou !!");
            }else {
                JOptionPane.showMessageDialog(null,"NÃ£o encontrou");
            }
            //System.out.println(emissora.getNome());
          
        
      
        
    }                                            
